// Exercice 4.1
class Livre {

    // Attributs :
    String isbn;
    String titre;
    String auteur;
    double prix;
    int annee;
    int page;

    // Constructeur :
    Livre(String nIsbn, String nTitre, String nAuteur, double nPrix, int nAnnee, int nPage ) {

        isbn = nIsbn;
        titre = nTitre;
        auteur = nAuteur;
        prix = nPrix;
        annee = nAnnee;
        page = nPage;
    }
}
